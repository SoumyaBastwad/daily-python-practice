# Data Types in Python 
# Data types tell which types of value hold by variables like integer, float, str etc. 
# In python there is no need to define or declare data type explicitly or externally in program
# Data type basically depends on the value we provide because according to value internally the data type class will be assigned automatically.
# Predefined data types/ Fundamental data types:
# int
# float
# complex
# bool 
# str 

#This example is based on all the predefined data types and how to use it. 
name = 'soumya bastwad'       
marks = 80    
percentage = 93.5    
complex_value= 2+8j    
passed     = True    
print('Display the output:')    
print('Name of Person:', name)     #Name of Person: soumya bastwad
print('Marks                 :', marks)     #Marks : 80 
print('Percentage       :', percentage)    #Percentage : 93.5 
print('Complex value :', complex_value)    #Complex value : (2+8j) 
print('Passed status   :', passed)          #Passed status : True


#Python is a dynamically typed language
